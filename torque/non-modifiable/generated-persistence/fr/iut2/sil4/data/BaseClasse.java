    {
        copyObj.setClasseId(0);
        copyObj.setNom(nom);

        if (deepcopy)
        {
            if (collEtudiants != null)
            {
                for (int i = 0; i < collEtudiants.size(); i++)
                {
                    Etudiant obj = collEtudiants.get(i);
                    copyObj.addEtudiant(obj.copy());
                }
            }
            else
            {
                copyObj.collEtudiants = null;
            }
            if (collMatiereClasses != null)
            {
                for (int i = 0; i < collMatiereClasses.size(); i++)
                {
                    MatiereClasse obj = collMatiereClasses.get(i);
                    copyObj.addMatiereClasse(obj.copy());
                }
            }
            else
            {
                copyObj.collMatiereClasses = null;
            }
        }
        return copyObj;
    }
        
    
